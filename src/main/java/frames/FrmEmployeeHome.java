    private void SubmitRequest(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SubmitRequest
        // Check if all required fields are filled
        if (txtEmployeeID.getText().isEmpty() || 
            txtLastName.getText().isEmpty() || 
            txtFirstName.getText().isEmpty() ||
            dcRequested.getDate() == null ||
            dcStartDate.getDate() == null ||
            dcEndDate.getDate() == null ||
            cbLLeaveType.getSelectedIndex() == 0 ||
            cbLDays.getSelectedIndex() == 0 ) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Incomplete Information", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String filename = "C:\\Users\\Anna\\Downloads\\leaveRecord.csv";
        employee.setEmployeeID(Integer.parseInt(txtEmployeeID.getText()));
        employee.setLastName(txtLastName.getText());
        employee.setFirstName(txtFirstName.getText());
        
        // Get the date requested from the date picker
        Date request = dcRequested.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy");
        String dateRequested = dateFormat.format(request);
        
        // Get leave type and days from JComboBox
        String leaveType = cbLLeaveType.getSelectedItem().toString();
        String days = cbLDays.getSelectedItem().toString();
        
        // Get start and end dates from JDateChooser
        Date startDate = dcStartDate.getDate();
        Date endDate = dcEndDate.getDate();
        
        // Format start and end dates
        String startDateStr = dateFormat.format(startDate);
        String endDateStr = dateFormat.format(endDate);
        
        // Get reason for leave request
        String reason = txtReason.getText();
        
        try {
            LeaveManagement leave = new LeaveManagement();
            EmployeeRecord record = new EmployeeRecord();
            employee.setEmployeeID(Integer.parseInt(txtEmployeeID.getText()));
            
            // Read leave record to get remaining leave balances
            CSVReader csvreader = new CSVReader(new FileReader(filename));
            csvreader.readNext();
            
            String [] line;
            String remainSL = null;
            String remainVL = null;
            String remainEL = null;
        
            // Find the employee's record and get remaining leave balances
            while((line = csvreader.readNext())!=null){
                if(Integer.parseInt(line[0]) == employee.getEmployeeID()){
                remainSL = line[3];
                remainVL = line[4];
                remainEL = line[5];
                break;
            }
        }
        csvreader.close();
        
        int empoyeeID = Integer.parseInt(txtEmployeeID.getText());
        
        if(leave.applyForLeave(filename, String.valueOf(employee.getEmployeeID()), leaveType, days)) {
           leave.calculateLeaveBalance(filename, String.valueOf(employee.getEmployeeID()), leaveType, days);
           
           // Create leave file witl all necessary information
           record.createLeaveFile(dateRequested, txtEmployeeID.getText(), txtLastName.getText(), txtFirstName.getText(), leaveType, days, startDateStr, endDateStr, remainSL, remainVL, remainEL, reason);
           JOptionPane.showMessageDialog(this,"Leave is Submitted Successfully!","Leave Application", JOptionPane.INFORMATION_MESSAGE);
    
           cbLLeaveType.setSelectedIndex(0);
           cbLDays.setSelectedIndex(0);
           dcRequested.setDate(null);
           dcStartDate.setDate(null);
           dcEndDate.setDate(null);
           txtReason.setText("");
        
           // Update the available leave balances     
           txtSLAvail.setText(remainSL);
           txtVLAvail.setText(remainVL);
           txtELAvail.setText(remainEL);
           
        } else {
            JOptionPane.showMessageDialog(this, "The requested leave exceeds your available leave balance.","Leave Request Error", JOptionPane.ERROR_MESSAGE);
        }
        
        } catch (IOException | CsvValidationException ex) {
            Logger.getLogger(FrmEmployeeHome.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        refreshLeaveTable();
    }//GEN-LAST:event_SubmitRequest

    private void loadLeave(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_loadLeave
        try {
            DefaultTableModel leaveModel = record.fetchLeave("leaveApplication.csv");
            
            DefaultTableModel leave = new DefaultTableModel(new Object[]{"Request Date", "Leave Type", "Days", "Start Date", "End Date", "Status"}, 0);
            
            for (int i = 0; i < leaveModel.getRowCount(); i++) {
                Object[] rowData = new Object[6]; // Number of columns for tblLeave
                   rowData[0] = leaveModel.getValueAt(i, 0);
                rowData[1] = leaveModel.getValueAt(i, 4);
                rowData[2] = leaveModel.getValueAt(i, 5);
                rowData[3] = leaveModel.getValueAt(i, 6);
                rowData[4] = leaveModel.getValueAt(i, 7);
                rowData[5] = leaveModel.getValueAt(i, 8);
                leave.addRow(rowData);
            }
            
            // Set the model for tblLeave
            tblLeave.setModel(leave);
            adjustColumns(tblLeave);
        } catch (IOException | CsvValidationException ex) {
            Logger.getLogger(FrmEmployeeHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loadLeave

    private void generatePayslip(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_generatePayslip
        try {
            String csvFilename = "payrollPayslip.csv";
            
            // Get selected start and end dates
            String selectedStartDate = String.valueOf(cbStartDate.getSelectedItem());
            String selectedEndDate = String.valueOf(cbEndDate.getSelectedItem());
            
            if (selectedStartDate.equals("- Start Date -") || selectedEndDate.equals("- End Date -")) {
            JOptionPane.showMessageDialog(null, "Please select valid start and end dates.", "Invalid Dates", JOptionPane.ERROR_MESSAGE);
            return;
            }
            
            // Fetch payslip details based on the selected dates
            String[] payslipDetail = record.fetchPayslip(csvFilename, selectedStartDate, selectedEndDate, employee.getEmployeeID());
            
            if (payslipDetail != null) {
                txtMonthlyRate.setText(payslipDetail[3]);
                txtDailyRate.setText(payslipDetail[4]);
                txtDaysWorked.setText(payslipDetail[5]);
                txtOvertime.setText(payslipDetail[6]);
                txtGrossIncome.setText(payslipDetail[7]);
                txtRiceSubsidy.setText(payslipDetail[8]);
                txtPhoneAllowance.setText(payslipDetail[9]);
                txtBenefitsTotal.setText(payslipDetail[10]);
                txtDSSS.setText(payslipDetail[11]);
                txtDPhilhealth.setText(payslipDetail[12]);
                txtDPagibig.setText(payslipDetail[13]);
                txtDTax.setText(payslipDetail[14]);
                txtDTotal.setText(payslipDetail[15]);
            } else {
                JOptionPane.showMessageDialog(this, "No payslip data found for the selected dates.", "No Data", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (IOException | CsvValidationException ex) {
            JOptionPane.showMessageDialog(this, "Error reading payslip data.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException ex) {
            Logger.getLogger(FrmEmployeeHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_generatePayslip

