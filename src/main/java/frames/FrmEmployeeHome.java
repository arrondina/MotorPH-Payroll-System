    private DefaultTableModel reverseTableModel(DefaultTableModel model) {
        DefaultTableModel reversedModel = new DefaultTableModel(model.getRowCount(), model.getColumnCount());
        
        for (int col = 0; col < model.getColumnCount(); col++) {
            reversedModel.addColumn(model.getColumnName(col));
        }
        
        for (int i = 0; i < model.getRowCount(); i++) {
            for (int j = 0; j < model.getColumnCount(); j++) {
                reversedModel.setValueAt(model.getValueAt(model.getRowCount() - 1 - i, j), i, j);
            }
        }
        
        return reversedModel;
    }
    
    
    
